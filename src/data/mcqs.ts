// Fisher-Yates shuffle
export function shuffleArray<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// Generate a set of 100 MCQs (shuffle from bank, take 100)
export function generateMCQSet(setIndex: number): MCQ[] {
  // Use setIndex as seed bias by rotating the bank first
  const rotated = [...mcqBank.slice(setIndex % mcqBank.length), ...mcqBank.slice(0, setIndex % mcqBank.length)];
  return shuffleArray(rotated).slice(0, Math.min(100, rotated.length));
}
